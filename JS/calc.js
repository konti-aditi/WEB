

let num1 = parseInt(prompt("Enter number1"))

let num2 = parseInt(prompt("Enter number2"))


let operation = prompt("arithmatic operation to perform")


if(operation==="+"){

    console.log(num1+num2)
    
}
if(operation==="-"){

    console.log(num1-num2)
    
}
if(operation==="*"){

    console.log(num1*num2)
    
}
if(operation==="/"){

    console.log(num1/num2)
    
}
if(operation==="%"){

    console.log(num1%num2)
    
}